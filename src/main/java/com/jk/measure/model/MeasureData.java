package com.jk.measure.model;

public class MeasureData {

    private MeasureDataInfo year;
    private MeasureDataInfo season;
    private MeasureDataInfo month;
    private MeasureDataInfo day;
    private MeasureDataInfo zhen;
    private MeasureDataInfo time;

    public MeasureDataInfo getYear() {
        return year;
    }

    public void setYear(MeasureDataInfo year) {
        this.year = year;
    }

    public MeasureDataInfo getSeason() {
        return season;
    }

    public void setSeason(MeasureDataInfo season) {
        this.season = season;
    }

    public MeasureDataInfo getMonth() {
        return month;
    }

    public void setMonth(MeasureDataInfo month) {
        this.month = month;
    }

    public MeasureDataInfo getDay() {
        return day;
    }

    public void setDay(MeasureDataInfo day) {
        this.day = day;
    }

    public MeasureDataInfo getZhen() {
        return zhen;
    }

    public void setZhen(MeasureDataInfo zhen) {
        this.zhen = zhen;
    }

    public MeasureDataInfo getTime() {
        return time;
    }

    public void setTime(MeasureDataInfo time) {
        this.time = time;
    }
}

package com.jk.measure.model;

public class ForTune {

    private String jizStr;

    private String month;

    private String year;

    public String getJizStr() {
        return jizStr;
    }

    public void setJizStr(String jizStr) {
        this.jizStr = jizStr;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }
}
